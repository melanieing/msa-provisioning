#!/bin/bash
# ─────────────────────────────────────────────────────────────────────────
# 이 파일은 뭐 하는 파일?
# ─────────────────────────────────────────────────────────────────────────
# Bastion + 모든 EC2 노드가 공유할 SSH 키 페어를 로컬에 생성.
#
# 언제 실행?
#   처음 한 번만. terraform apply 전에 미리 실행해두면 됨.
#   (main.tf 의 aws_key_pair 가 이 파일이 만든 .pub 파일을 참조)
#
# 결과:
#   ~/.ssh/ktcloud-bastion-node-key       (개인키, 본인만 보관)
#   ~/.ssh/ktcloud-bastion-node-key.pub   (공개키, AWS 에 업로드됨)
#
# 옵션 설명:
#   -t rsa       : RSA 알고리즘 사용
#   -b 4096      : 4096 비트 키 (안전)
#   -f <path>    : 저장 위치
#   -N ""        : passphrase 없음 (자동화 친화적, 단 키 유출 시 즉시 위험)
# ─────────────────────────────────────────────────────────────────────────

ssh-keygen -t rsa -b 4096 -f ~/.ssh/ktcloud-bastion-node-key -N ""

# 개인키 권한을 본인만 읽기로 잠금. SSH 가 너무 열린 권한이면 키 사용 거부.
chmod 600 ~/.ssh/ktcloud-bastion-node-key
